// Declare an integer variable and assign it with the value 254 in hexadecimal format. 
 


using System;

    class DeclareHexValue
    {
        static void Main()
        {
            int HexNumber = 0xFE;  //FE is the Hex value of 254
            Console.WriteLine("The hexadecimal value of 254 is {0:X}", HexNumber);
        }
    }

